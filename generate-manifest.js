/*
  Run this with:  node generate-manifest.js
  from inside the family-photobooth folder.

  It scans the /photos folder and rewrites photos.js so you don't have to
  type 50+ filenames by hand. Captions default to a cleaned-up version of
  the filename — edit them in photos.js afterward if you want nicer text.

  Sorts files alphabetically, so name your files like:
    01-backyard.jpg, 02-beach-day.jpg, 03-birthday.jpg ...
  if you care about the order they appear in.
*/

const fs = require("fs");
const path = require("path");

const PHOTOS_DIR = path.join(__dirname, "photos");
const OUTPUT_FILE = path.join(__dirname, "photos.js");
const VALID_EXT = [".jpg", ".jpeg", ".png", ".webp", ".gif", ".heic"];

function toCaption(filename) {
  const base = filename.replace(path.extname(filename), "");
  return base
    .replace(/^\d+[-_\s]*/, "")
    .replace(/[-_]+/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .replace(/\b\w/g, (c) => c.toUpperCase());
}

function main() {
  if (!fs.existsSync(PHOTOS_DIR)) {
    console.error(`No "photos" folder found at ${PHOTOS_DIR}. Create it and add your images first.`);
    process.exit(1);
  }

  const files = fs
    .readdirSync(PHOTOS_DIR)
    .filter((f) => VALID_EXT.includes(path.extname(f).toLowerCase()))
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));

  if (!files.length) {
    console.error("No image files found inside /photos. Add some and try again.");
    process.exit(1);
  }

  const entries = files
    .map((f) => `  { src: "photos/${f}", caption: "${toCaption(f).replace(/"/g, '\\"')}" },`)
    .join("\n");

  const output = `/* Auto-generated by generate-manifest.js — edit captions below if you like. */

const PHOTOS = [
${entries}
];
`;

  fs.writeFileSync(OUTPUT_FILE, output, "utf8");
  console.log(`Wrote ${files.length} photos to photos.js`);
}

main();
